package ir.ac.ut.ie.CA_06_mzFoodDelivery.utils;

public class CustomPair {
    String first;
    String second;
    public CustomPair(String first, String second) {
        this.first = first;
        this.second = second;
    }

    public String getFirst() {
        return first;
    }

    public String getSecond() {
        return second;
    }
}
